#include "includes/libft.h"

char		*ft_strndup(const char *s1, size_t n)
{
	int		i;
	char	*cpy;

	cpy = ft_strnew(n);
	i = 0;
	while (s1[i] && n)
	{
		cpy[i] = s1[i];
		i++;
		n--;
	}
	return (cpy);
}
